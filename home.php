		<p>The FIFA World Cup is a professional football tournament held between national football teams, Organised by FIFA.</p>
		<p>France are the defending champions, having defeated Croatia 4-2 in the 2018 final. At an estimated cost of over $220 billion, it is the most expensive World Cup ever held; this figure is disputed by Qatari officials, including organising CEO Nasser Al Khater, who said the true cost is $8 billion, and other figures relate to overall infrastructure development since the World Cup was awarded to Qatar in 2010. </p>
		<p>This tournament is set to be the last with 32 participating teams, with the field to increase to 48 teams for the 2026 edition.</p>
		<p>To avoid the extremes of Qatar's hot climate, this World Cup is being held during November and December. It is being played in a reduced timeframe of 29 days with 64 matches to be played in eight venues across five cities. </p>
		<p>The Qatar national football team entered the event automatically, their first World Cup, alongside 31 teams who were determined by the qualification process.</p>
		<p>Qatar lost all three group matches; they became the first hosts to lose every game, the earliest host nation eliminated, and the second host (after South Africa in 2010) not to progress past the first stage.</p>
