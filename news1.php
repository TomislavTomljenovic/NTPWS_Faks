				<h2>Croatia's victory over Japan</h2>
				<p><b>Croatia knock Japan out of World Cup after Livakovic’s heroics in shootout</b></p>
				<p>Farewell Samurai Blue. Japan have enriched the World Cup with stunning comebacks against old champions, indefatigable spirit and a passionate desire to upset the football hierarchy, but the formidable weight of history proved too great in Qatar. Their own, and Croatia’s.</p>
				<p>History repeated itself in this last‑16 tie as Croatia prevailed in a penalty shootout after extra time.</p>
				<p>Six of their past seven knockout games at major tournaments have lasted 120 minutes, with the exception of defeat against France in the 2018 World Cup final, and the smallest country left in the competition are defying the odds yet again.</p>
				<p>Japan exited agonisingly short of a first ever quarter-final appearance.</p>
				<p>The goalkeeper Dominik Livakovic was the Croatia hero with three saves from Takumi Minamino, Kaoru Mitoma and the captain, Maya Yoshida, each penalty struck low and without conviction.</p>
				<p>Moriyasu and his Croatia counterpart Zlatko Dalic understandably singled out the Dinamo Zagreb keeper as the contest’s defining influence but Livakovic put it best himself. “I don’t think these penalties were too difficult to defend,” he admitted. “They shot in a strange manner. These were not perfect penalties.”</p>
				<p>Japan’s spot-kicks were as tame as the preceding 120 minutes. The game itself was a monumental letdown before the Croatia substitute Mario Pasalic sent his country into the quarter-finals with the decisive penalty.</p>
