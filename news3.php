				<h2>Morocco’s World Cup magic potion: Football parents and fans</h2>
				<p><b>Forget Wags. The secret of Morocco’s success at World Cup 2022 might come down to the players’ parents who have joined them.</b></p>
				<p>After Morocco shocked the footballing world by defeating pre-tournament favourites Belgium 2-0 in a Group F play, Paris Saint-Germain fullback Achraf Hakimi made a beeline for his mother in the stands.</p>
				<p>Their embrace and subsequent cheek-pinch went viral across social media platforms. The 24-year-old footballer later posted an Instagram photo of him planting a kiss on his mother’s forehead with the caption, “I love you, Mom”.</p>
				<p>But Hakimi’s mother is not the only Moroccan football parent who has travelled to Qatar to support her son at the 2022 World Cup.</p>
				<p>On the instructions of coach Walid Regragui and Royal Moroccan Football Federation president Fouzi Lekjaa, family members chosen by all Moroccan squad members are entitled to an all-inclusive trip to Qatar.</p>
				<p>As a result, Morocco’s base at the Wyndham Doha West Bay hotel occasionally feels like an adorable parent-run summer camp. For some, like Regragui’s mother, Fatima, the trip was a once-in-a-lifetime opportunity.</p>
